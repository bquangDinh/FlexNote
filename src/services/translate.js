export const keyLabelLangs = Object.assign({}, ...Object.keys(languages).map(x => ({ [x]: {key:x, label: languages[x]} })));

export async function Translate(sourceLang, targetLang, text){
    //pre-process input text
    //remove any unnessecery whitespaces
    text = text.trim();

    const TRANSLATION_API_KEY = process.env.TRANSLATION_API_KEY;

    let response = {
        sourceLang: sourceLang,
        targetLang: targetLang,
        sourceText: text,
        translatedText: ''
    }

    if(!TRANSLATION_API_KEY){
        console.error('Translation api key not found !!!');

        //return an empty response
        return response;
    }

    let apiURL = `https://www.googleapis.com/language/translate/v2?key=${TRANSLATION_API_KEY}`;
    let params = {
        source: sourceLang,
        target: targetLang,
        q: text
    };

    Object.keys(params).forEach(function(key) {
        apiURL += '&' + key + '=' + params[key];
    });

    await fetch(apiURL)
    .then(function(_response) { return _response.json() })
    .then(function(_response) {
        response.translatedText = _response.data.translations[0].translatedText;
    })
    .catch(function(err) { console.error(err); });

    return response;
}